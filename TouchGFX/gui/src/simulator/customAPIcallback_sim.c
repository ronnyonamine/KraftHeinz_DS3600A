/**
 * API: CST_APIcbSendBTBatStatus()
 */
int8_t CST_APIcbGetBackendStatus(uint16_t *status) 
{
  /* If a new dummy status has been generated, send it to UI */
  if (new_status)
  {
    *status = dummy_status;
    new_status = false;
    return true;
  }
  return false;
}

/**
 * API: CST_APIcbSendBTBatStatus()
 */
int8_t CST_APIcbSendBackendStatus(uint16_t *status)
{
  /* Store a new dummy status for UI to retrieve */
  dummy_status = *status;
  new_status = true;
  return true;
}

