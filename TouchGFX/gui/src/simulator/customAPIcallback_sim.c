/* buffer that will hold dummy application data */

static char dummy_data[MAX_DUMMY_BARCODE_SIZE+1] = {0};
static uint16_t dummy_status = 0;

/* internal flag to know if a new dummy data has been generated by simulator */

static bool new_scan = false;
static bool new_status = false;

/* count seed for Random/dummy function */
static uint32_t count = 0;

/** @} */
