/**
 * API: CST_APIcbSendDataToGUI()
 */
int8_t CST_APIcbSendDataToGUI(BarcodeRecord_t *const dest)
{
  uint8_t dummy_len;
  int i = 0;
  /* Generate new dummy data - seed new value */
  srand(count);
  count++;
  // modified for test purposes
  if ((count == 2) || (count == 6) || (count == 10))
  {
	  new_scan = true;
	  return true;
  }
  
  /* generate dummy length */
  dummy_len = (rand() & 0x000000FF) >> 2;
  if (dummy_len < MIN_DUMMY_BARCODE_SIZE)
  {
	dummy_len = MIN_DUMMY_BARCODE_SIZE;
  }
  
  for(i = 0; i < dummy_len; i++)
  {
	  dummy_data[i] = (uint8_t)('0' + (rand() % 10));
  }
  dummy_data[i] = 0; /* Terminate random string of numbers*/
  
  /* update internal flag to let know the poling function we have updated data */
  new_scan = true;
  
  return true;
}

/**
 * API: CST_APIcbGetDecodeData()
 */
char *CST_APIcbGetDecodeData(char **getMsg)
{
  /* If we have generated new dummy data */
  if(new_scan)
  {
	/* Link it */
	*getMsg = dummy_data;
	new_scan = false;
    return *getMsg;
  }
  
  return NULL;
}

